package ClaseEdu;

public class Bucles {

   
    public static void main(String[] args) {
     /*Bucle while */
     int c=5;
     int b=0;
     while (b!=c) {
        System.out.println("a");
        b+=1;
     }



     /*Bucle for */
     int a = 0;
     for(int i = 1; i <= a; i++){
        System.out.println("a");
     } 
    }
}   
    

